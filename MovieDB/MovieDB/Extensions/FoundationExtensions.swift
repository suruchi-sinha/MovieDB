import Foundation

extension CGFloat {
    
    enum Spacing {
        static let half: CGFloat = 4.0
        static let one: CGFloat = 8.0
        static let two: CGFloat = one * 2
        static let three: CGFloat = one * 3
    }
    
    enum Sizing {
        static let small: CGFloat = 8.0
        static let medium: CGFloat = 16.0
        static let large: CGFloat = 24.0
    }
}
